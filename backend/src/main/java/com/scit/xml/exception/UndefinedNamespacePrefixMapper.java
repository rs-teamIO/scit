package com.scit.xml.exception;

public class UndefinedNamespacePrefixMapper extends Exception {

	private static final long serialVersionUID = 1L;

	public UndefinedNamespacePrefixMapper() {
		super();
	}

	public UndefinedNamespacePrefixMapper(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public UndefinedNamespacePrefixMapper(String message, Throwable cause) {
		super(message, cause);
	}

	public UndefinedNamespacePrefixMapper(String message) {
		super(message);
	}

	public UndefinedNamespacePrefixMapper(Throwable cause) {
		super(cause);
	}


	

}
